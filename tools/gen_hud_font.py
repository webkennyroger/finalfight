def encode_tile(pixels):
    """Encode 64-pixel list to SNES 2bpp tile (16 bytes)."""
    tile = bytearray(16)
    for row in range(8):
        b0 = 0
        b1 = 0
        for col in range(8):
            pal = pixels[row * 8 + col]
            bit = 7 - col
            b0 |= (pal & 1) << bit
            b1 |= ((pal >> 1) & 1) << bit
        tile[row * 2] = b0
        tile[row * 2 + 1] = b1
    return tile

