from PIL import Image
import sys, os

DIGITS = {
    '0': [0x7C, 0xC6, 0xC6, 0xC6, 0xC6, 0xC6, 0xC6, 0x7C],
    '1': [0x30, 0x70, 0x30, 0x30, 0x30, 0x30, 0x30, 0xFC],
    '2': [0x7C, 0xC6, 0x06, 0x0C, 0x38, 0x60, 0xC0, 0xFE],
    '3': [0xFE, 0x0C, 0x18, 0x3C, 0x06, 0x06, 0xC6, 0x7C],
    '4': [0x18, 0x38, 0x78, 0xD8, 0xD8, 0xFE, 0x18, 0x18],
    '5': [0xFE, 0xC0, 0xC0, 0xFC, 0x06, 0x06, 0xC6, 0x7C],
    '6': [0x3C, 0x60, 0xC0, 0xFC, 0xC6, 0xC6, 0xC6, 0x7C],
    '7': [0xFE, 0x06, 0x0C, 0x18, 0x30, 0x30, 0x30, 0x30],
    '8': [0x7C, 0xC6, 0xC6, 0x7C, 0xC6, 0xC6, 0xC6, 0x7C],
    '9': [0x7C, 0xC6, 0xC6, 0x7E, 0x06, 0x06, 0x0C, 0x78],
}

WIDTH = 80
HEIGHT = 8

img = Image.new('P', (WIDTH, HEIGHT))
pal = [0]*768
pal[0:3] = [0,0,0]
pal[3:6] = [255,255,255]
img.putpalette(pal)

for d_idx in range(10):
    digit = DIGITS[str(d_idx)]
    x_off = d_idx * 8
    for y in range(HEIGHT):
        row = digit[y]
        for x in range(8):
            px = (row >> (7-x)) & 1
            img.putpixel((x_off + x, y), 1 if px else 0)

out = sys.argv[1] if len(sys.argv) > 1 else 'assets/hud/digits.png'
img.save(out)
print(f"Saved {out}")
